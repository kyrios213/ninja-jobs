<template>
  <div v-if="job">
    <h1>{{ job.title }}</h1>
    <p>The job id is {{ id }}</p>
    <p>{{ job.details }}</p>
  </div>

  <div v-else>
    <p>Loading jobs details</p>
  </div>
</template>

<script>
export default {
  props: ["id"],
  data() {
    return {
      job: null,
    };
  },
  async mounted() {
    try {
      const res = await fetch(`http://localhost:3000/jobs/${this.id}`);
      const data = await res.json();
      this.job = data;
    } catch (err) {
      console.log(err.message);
    }
  },
  //   data() {
  //     return {
  //       id: this.$route.params.id,
  //     };
  //   },
};
</script>

